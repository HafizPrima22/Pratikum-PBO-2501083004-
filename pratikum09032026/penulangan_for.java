public class penulangan_for {
    public static void main(String[] args) {
        System.out.println("perulangan 1 sampai 10");
        for(int i=1;i<=10;i++)
            {
                System.out.print("\t"+i);
            }
        System.out.println("\nperulangan 10 sampai 1");
        for(int i=10;i>=1;i--)
        {
            System.out.print("\t"+i);
        }
    }
    
}
